// Relational operator ->  > , < , >= , <= , == , === , != , !==

// Logical Operator ->   && , || , !

var result;
result = (20<33);
document.write("(20<33) ::  "+"" + result + "<br><br>");

result = (20>33);
document.write("(20>33) ::  "+"" + result + "<br><br>");

result = (20==20);
document.write("(20==20) ::  "+"" + result + "<br><br>");

result = (20==="20");
document.write("(20==='20') ::  "+"" + result + "<br><br>");

result = (40<=40);
document.write("(40<=40) ::  "+"" + result + "<br><br>");

result = (30>=30);
document.write("(30>=30) ::  "+"" + result + "<br><br>");

result = (!(20<33));
document.write(" (!(20<33)) ::  "+"" + result + "<br><br>");

result = (21!=26);
document.write("(21!=26) ::  "+"" + result + "<br><br>");

result = (21!=="26");
document.write("(21!=='26') ::  "+"" + result + "<br><br>");

result = (21=="26");
document.write("(21=='26') ::  "+"" + result + "<br><br>");
